package Operators;

public class operator5 {
    public static void main(String args[]){
        // logical operator
        // && -->  *
        // || --> +
        // !

        System.out.println(3>2 && 1!=1); // both condition must be true
        System.out.println(3>2 || 1!=1); // one condition must be true
        System.out.println(! (3<2)); // opposite
    }
    
}
